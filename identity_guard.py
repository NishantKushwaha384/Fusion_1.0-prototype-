# Pre-compile all patterns
COMPILED_PATTERNS: List[Tuple[List[re.Pattern], Callable[[str], str]]] = [
    (
        [re.compile(p, re.IGNORECASE | re.DOTALL) for p in patterns],
        fn,
    )
    for patterns, fn in RAW_PATTERNS
]

# ── THE GUARD ────────────────────────────────────────────────

def check_identity_guard(question: Any) -> Optional[Dict[str, Any]]:
    """
    Intercepts identity/meta questions and returns a hardcoded response dict.
    Returns None if no match — allowing the question to pass to the LLMs.
    """
    try:
        if not isinstance(question, str) or not question.strip():
            return None

        norm_q = normalize(question)

        for regex_list, response_fn in COMPILED_PATTERNS:
            for pattern in regex_list:
                if pattern.search(norm_q):
                    answer = response_fn(question)
                    logger.info(f"🛡️  [IdentityGuard] Matched: '{pattern.pattern}'")
                    return {
                        "final_answer":  answer,
                        "category":      "system_meta",
                        "strategy":      "identity_guard",
                        "confidence":    1.0,
                        "answerer":      "Fusion 1.0 Identity Guard",
                        "source":        "system",
                        "metadata": {
                            "system_version": SYSTEM_IDENTITY["name"],
                            "is_intercepted": True,
                            "matched_pattern": pattern.pattern,
                            "answering_component": "identity_guard",
                            "response_type": "meta_knowledge",
                        },
                    }

        return None  # No match → pass to LLMs

    except Exception as e:
        logger.error(f"🚨 [IdentityGuard Error] {e}")
        return None  # Failsafe: never break the app


