# ── NORMALIZATION ─────────────────────────────────────────────

# FIX: 'u'→'you' and 'r'→'are' are only safe as FULL standalone tokens.
# We handle them carefully to avoid corrupting real words.
_CONTRACTIONS = {
    r"who's":   "who is",
    r"what's":  "what is",
    r"whats":   "what is",
    r"who're":  "who are",
    r"you're":  "you are",
    r"ur":      "you are",
    r"i'm":     "i am",
    r"aren't":  "are not",
    r"isn't":   "is not",
    r"don't":   "do not",
    r"doesn't": "does not",
    r"can't":   "can not",
    r"whos":    "who is",
    # Slang — only expand single-char tokens when surrounded by word boundaries
    # 'u' and 'r' are handled separately below to avoid corrupting other words
}

# Slang substitutions that are safe only as isolated words
_SAFE_SLANG = {
    r"\bu\b":   "you",
    r"\br\b":   "are",
    r"\bwat\b": "what",
    r"\bwot\b": "what",
    r"\bwru\b": "who are you",
    r"\bwuu2\b":"what are you",
    r"\bwho r u\b": "who are you",
}

def normalize(text: str) -> str:
    """Expand contractions and clean text without corrupting real words."""
    if not isinstance(text, str):
        return ""

    text = text.lower().strip()

    # Expand contractions (order matters — longer tokens first)
    for token, replacement in _CONTRACTIONS.items():
        text = re.sub(rf"\b{re.escape(token)}\b", replacement, text)

    # Safe slang (already have \b in keys)
    for pattern, replacement in _SAFE_SLANG.items():
        text = re.sub(pattern, replacement, text)

    # Strip punctuation except internal apostrophes already handled
    text = re.sub(r"[^\w\s]", " ", text)

    # Collapse whitespace
    text = re.sub(r"\s+", " ", text).strip()

    return text

